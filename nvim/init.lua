-- ============================================================
-- Neovim + LazyVim 新手配置总说明
-- ============================================================
--
-- 欢迎！这是一套基于 LazyVim 的 Neovim 配置。
-- LazyVim 已经帮你预装了大量实用功能（文件树、模糊搜索、
-- 自动补全、LSP 语法分析、Git 集成、主题等），开箱即用。
--
-- ============================================================
-- 目录结构说明（你需要改东西时，找对应文件就行）
-- ============================================================
--
-- ~/.config/nvim/
-- ├── init.lua                    ← 你现在看的这个文件（入口）
-- ├── lua/
-- │   ├── config/
-- │   │   ├── lazy.lua            ← 插件管理器 lazy.nvim 的引导（一般不用改）
-- │   │   ├── options.lua         ← 编辑器选项（行号、缩进、搜索等）
-- │   │   ├── keymaps.lua         ← 自定义快捷键
-- │   │   └── autocmds.lua        ← 自动命令（保存去空格、打开恢复位置等）
-- │   └── plugins/
-- │       ├── example.lua         ← 官方示例（仅供参考，不会加载）
-- │       └── custom.lua          ← ★ 你自己加的插件放这里 ★
-- └── stylua.toml                 ← Lua 代码格式化配置
--
-- ============================================================
-- 新手常见操作
-- ============================================================
--
-- 1. 打开 Neovim：              nvim
-- 2. 打开文件：                  nvim 文件名
-- 3. 退出：                      :q   或  :qa
-- 4. 保存：                      :w   或  Control+s（已配置）
-- 5. 保存并退出：                :wq
--
-- 6. 打开文件树：                空格 + e
-- 7. 搜索文件：                  空格 + f + f
-- 8. 全局搜索文字：              空格 + f + g（或 空格 + /）
-- 9. 打开 lazygit：              空格 + g + g
-- 10. 打开插件管理：             空格 + l
--
-- 模式切换：
-- - i         进入编辑模式
-- - Esc       回到普通模式
-- - v         进入可视（选择）模式
-- - :         进入命令模式
--
-- ============================================================
-- 想加新插件？
-- ============================================================
-- 编辑 ~/.config/nvim/lua/plugins/custom.lua
-- 格式参考那个文件里的注释。
-- 改完重启 nvim，lazy.nvim 会自动安装。
--
-- ============================================================
-- 想换主题？
-- ============================================================
-- 在 custom.lua 里改 colorscheme 即可（已有示例）。
--
-- ============================================================

